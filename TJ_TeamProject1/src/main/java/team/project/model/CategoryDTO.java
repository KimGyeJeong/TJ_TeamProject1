package team.project.model;

public class CategoryDTO {
	private int ca_no;
	private String ca_name;
	private int ca_grp;
	private int ca_level;
	
	
	public int getCa_no() {
		return ca_no;
	}
	public void setCa_no(int ca_no) {
		this.ca_no = ca_no;
	}
	public String getCa_name() {
		return ca_name;
	}
	public void setCa_name(String ca_name) {
		this.ca_name = ca_name;
	}
	public int getCa_grp() {
		return ca_grp;
	}
	public void setCa_grp(int ca_grp) {
		this.ca_grp = ca_grp;
	}
	public int getCa_level() {
		return ca_level;
	}
	public void setCa_level(int ca_level) {
		this.ca_level = ca_level;
	}
	
	
}
